print("Anna minulle kolme kokonaislukua")

kokonaisluku1= int(input("numero1: "))
kokonaisluku2= int(input("numero2: "))
kokonaisluku3= int(input("numero3: "))

summa= kokonaisluku1 + kokonaisluku2 + kokonaisluku3
tulo= kokonaisluku1 * kokonaisluku2 * kokonaisluku3
keskiarvo= (kokonaisluku1 + kokonaisluku2 + kokonaisluku3) / 3

print("Näiden lukujen summa on:", summa)
print("Näiden lukujen tulo on:", tulo)
print("Näiden lukujen keskiarvo on:", keskiarvo)